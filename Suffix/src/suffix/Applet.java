    private boolean ekle(String kelime){
         List<String> Suffixler=new ArrayList<>();
         List<String> Prefixler=new ArrayList<>();
         String kelime2="";
         for (int i = kelime.length()-1; i>=0; i--) {
            kelime2=kelime2+kelime.charAt(i);  
        }
                
         for (int i = 0; i < kelime.length(); i++) {
             Suffixler.add(kelime.substring(i));     
         }
         for (int i = 0; i < kelime2.length(); i++) {
             String kelime3=kelime2.substring(i);
             String kelime4="";
             for (int j = kelime3.length()-1; j>=0; j--) {
                 kelime4=kelime4+kelime3.charAt(j);
             }            
             Prefixler.add(kelime4);            
         }
         for (int i = 0; i < Suffixler.size(); i++) {
             for (int j = 0; j < Prefixler.size(); j++) {            
                 if(Suffixler.get(i).equals(Prefixler.get(j))){
                     return true;
                 }      
             }          
        }
        return false;
    }
